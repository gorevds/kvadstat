# Тир города: «столичные» рынки vs регионы — у них разный масштаб цен, поэтому
# одни и те же ₽/м² значат разный класс.
_CAPITAL_CITIES = {"msk", "mo", "spb"}

# Абсолютные ценовые бэнды (₽/м²): верхняя граница → класс. Применяются только
# к будущим ЖК неизвестных застройщиков (известные закрыты курированием/дефолтом).
_BANDS_CAPITAL = ((230_000, "эконом"), (400_000, "комфорт"),
                  (600_000, "бизнес"), (900_000, "премиум"))
_BANDS_REGION = ((120_000, "эконом"), (200_000, "комфорт"),
                 (300_000, "бизнес"), (420_000, "премиум"))


def auto_class(city: str | None, meter_price: float) -> str:
    """Класс по медианной цене за м² с поправкой на тир города."""
    bands = _BANDS_CAPITAL if (city or "msk") in _CAPITAL_CITIES else _BANDS_REGION
    for threshold, label in bands:
        if meter_price < threshold:
            return label
    return "элит"


def classify(
    block_id: int,
    developer: str,
    city: str | None = None,
    meter_price: float | None = None,
) -> str | None:
    """Класс одного ЖК. None — если класс определить нечем (неизвестный
    застройщик без цены); в витрине это COALESCE'ится в 'н/д'."""
    if block_id in _CURATED:
        return _CURATED[block_id]
    if developer in _DEV_DEFAULT:
        return _DEV_DEFAULT[developer]
    if meter_price:
        return auto_class(city, meter_price)
    return None
